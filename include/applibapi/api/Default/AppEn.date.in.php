<?php



/***********************接口介绍说明******************************************
 * date.in  
 * 取系统时间
 * *****************************************************************************
 */


$m = Plug_Set_Data('m');  #获取系统时间格式，空默认 Y-m-d H:i:s

if ($m == '') $m = 'Y-m-d H:i:s';
Plug_Echo_Info(date($m, PLUG_UNIX()), 200);
